class EngineeringProps(BaseModel):
    """Normalized engineering properties for filtering"""
    material_family: Optional[MaterialFamily] = None
    
    # Range Queries (handled as separate min/max fields in Qdrant)
    temp_min_c: Optional[float] = Field(None, description="Min Temp Celsius")
    temp_max_c: Optional[float] = Field(None, description="Max Temp Celsius")
    
    pressure_min_bar: Optional[float] = Field(None, description="Min Pressure Bar")
    pressure_max_bar: Optional[float] = Field(None, description="Max Pressure Bar")
    
    standard_ids: List[str] = Field(default_factory=list, description="Associated Standards e.g. 'ISO 3601'")
    manufacturer: Optional[str] = None
    product_codes: List[str] = Field(default_factory=list, description="Specific article numbers")


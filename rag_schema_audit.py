class ChunkMetadata(BaseModel):
    """
    The flatten payload stored in Qdrant.
    Combines Doc-Level and Chunk-Level metadata.
    """
    # -- Identity (Strict) --
    tenant_id: str
    doc_id: str
    chunk_id: str
    chunk_hash: str = Field(..., description="SHA256 of content for idempotency")
    
    # -- Traceability --
    source_uri: str
    source_type: SourceType
    domain: Domain
    title: str
    page_number: Optional[int] = None
    section: Optional[str] = None # Breadcrumb: "Chapter 1 > Section A"
    
    # -- Versioning --
    ingest_version: int = 1
    is_current: bool = True
    created_at: float
    
    # -- Engineering --
    eng: Optional[EngineeringProps] = None
    
    # -- Content --
    text: str = Field(..., description="Full text content of chunk")
    citability: bool = True

    @staticmethod
    def generate_chunk_id(tenant_id: str, doc_id: str, index: int) -> str:
        """Deterministic ID generation for overwrite consistency (UUID5)"""
        raw = f"{tenant_id}:{doc_id}:{index}"
        return str(uuid.uuid5(uuid.NAMESPACE_DNS, raw))

    @staticmethod
    def compute_hash(text: str) -> str:
        """Content hash for deduplication"""
        # Normalize whitespace
        clean = " ".join(text.split())
        return hashlib.sha256(clean.encode()).hexdigest()
